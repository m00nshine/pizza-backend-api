CREATE VIEW [dbo].[vw_minimalTransactionDetails]
AS
SELECT        td.OrderId, SUM(td.Quantity * pp.Price) AS TotalPrice, t.CreatedDate
FROM            [order].transactionDetails AS td INNER JOIN
                         [order].transactions AS t ON t.Id = td.OrderId LEFT OUTER JOIN
                         pizza.pizzaPrices AS pp ON pp.LongCode = td.PizzaLongCode
GROUP BY td.OrderId, t.CreatedDate
GO
