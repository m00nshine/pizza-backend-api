CREATE VIEW [dbo].[vw_completeTransactionDetails]
AS
SELECT        t.OrderId, t.Quantity, pp.Price, t.Quantity * pp.Price AS Amount, pt.Name, pc.Name AS Category, ps.Code AS Size
FROM            [order].transactionDetails AS t LEFT OUTER JOIN
                         pizza.pizzaPrices AS pp ON pp.LongCode = t.PizzaLongCode LEFT OUTER JOIN
                         pizza.pizzaTypes AS pt ON pt.Id = pp.TypeId LEFT OUTER JOIN
                         pizza.categories AS pc ON pc.Id = pt.CategoryId LEFT OUTER JOIN
                         pizza.sizes AS ps ON ps.Id = pp.SizeId
GO
